package cl.colmena.common.wrapper;

public class BooleanWrapper {
	private boolean value;

	public BooleanWrapper() {
	}

	public BooleanWrapper(boolean value) {
		this.value = value;
	}

	public boolean isValue() {
		return value;
	}

	public void setValue(boolean value) {
		this.value = value;
	}
	
}
